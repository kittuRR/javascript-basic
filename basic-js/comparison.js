// Basic comparison operations

console.log(3 > 4);
console.log(7>= 6);
console.log(5 < 7);
console.log(6 <= 7);
console.log(3 == 3);
console.log(8 != 7);

// One datatype to another datatype comparison

console.log("4" > 6);
console.log(6 > "8");

// Null to another datatype comparison

console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);

//strict type comparison

console.log(3 === 3);
console.log(2 !== 6);

// Undefined datatype comparison

console.log(undefined > 0);
console.log(undefined < 0);
console.log(undefined == 0);













